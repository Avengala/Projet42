#include "libft.h"

void		ft_putendl(char const *s)
{
	int		i;
	char	letter;

	i = 0;
	while (s[i] != '\0')
	{
		letter = s[i];
		write(1, &letter, 1);
		i++;
	}
	write(1, "\n", 1);
}
